package com.example.chocosolver.problem;

/**
 * Represents arithmetic operators used in a problem.
 */
public enum Operator {
	/**
	 * Addition operator.
	 */
	ADD,

	/**
	 * Subtraction operator.
	 */
	SUBTRACT,

	/**
	 * Multiplication operator.
	 */
	MULTIPLY,

	/**
	 * Division operator.
	 */
	DIVIDE,

	/**
	 * Maximum operator.
	 */
	MAX,

	/**
	 * Minimum operator.
	 */
	MIN,

	/**
	 * Average operator.
	 */
	AVG
}
